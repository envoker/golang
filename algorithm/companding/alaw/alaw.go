func LinearToALaw(sample int16) uint8 {

	const clip int16 = 32635

	var exponent, mantissa, compandedValue uint8

	// Для симметрии максимальных отклонений
	if sample == -32768 {
		sample = -32767
	}

	var sign = uint8((sample >> 8) & 0x80)

	// abs(sample)
	if sample < 0 {
		sample = -sample
	}

	// clip
	if sample > clip {
		sample = clip
	}

	if sample > 255 {
		exponent = logTable[(sample>>8)&0x7F]
		mantissa = uint8((sample >> (exponent + 3)) & 0x0F)
		compandedValue = ((exponent << 4) | mantissa)
	} else {
		compandedValue = uint8(sample >> 4)
	}

	compandedValue ^= (sign ^ 0x55)

	return compandedValue
}

func ALawToLinear(b uint8) int16 {

	return alawDecompressTable[b]
}
